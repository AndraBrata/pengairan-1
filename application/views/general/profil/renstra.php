                                                <p><strong>Kata Pengantar</strong></p>
                                                <div>
                                                    <p>Penyusunan program kerja Jurusan Teknik Pengairan,&nbsp; didasarkan pada visi, misi, tujuan, sasaran dan strategi pencapaian yang telah ditetapkan oleh Jurusan Teknik Pengairan, serta mengacu kepada Rencana Strategis (Renstra) Universitas Brawijaya 2011 – 2020, Rencana Strategis (Renstra) Fakultas Teknik 2011 – 2020 serta Rencana Strategis (Renstra) Jurusan Teknik Pengairan 2011-2020. Penjabaran program didasarkan kepada tujuh bidang yang ditetapkan dalam Renstra Jurusan Teknik Pengairan.</p>
                                                    <p>Efektifitas dan efisiensi dari pelaksanaan proses pembelajaran di Jurusan Teknik Pengairan sangat tergantung dari komitmen sivitas akedemika Jurusan dalam implementasi dari Program Kerja yang telah dibuat.Kedepan tantangan serta persaingan Jurusan Teknik Pengairan dalam menghasilkan lulusan yang mampu berkiprah secara profesional berdasarkan kompetensi yang diperlukan semaik berat.</p>
                                                    <p>Oleh karena itu, Program Kerja Jurusan Teknik Pengairan tahun 2016&nbsp; ini diharapkan dapat dipahami dan dilaksanakan dengan baik oleh seluruh civitas Jurusan Teknik Pengairan dan pihak­-pihak lain yang terkait.</p>
                                                    <p>Malang, Januari 2016</p>
